package cn.edu.zucc.elevator.entity;

public class UserDistribution {
	//管理员数量
	private int managerCount;
	//单位管理员
	private int companyManagerCount;
	//超级管理员
	private int superManagerCount;
	//操作者
	private int operatorCount;
	//施工负责人
	private int constructorCount;
	//巡查人员
	private int inspectorCount;
	//维护人员
	private int maintainorCount;
	//检测人员
	private int testorCount;
	//一般用户
	private int userCount;
	//总人数
	private int allCount;
	
	
	public int getManagerCount() {
		return managerCount;
	}
	public void setManagerCount(int managerCount) {
		this.managerCount = managerCount;
	}
	public int getCompanyManagerCount() {
		return companyManagerCount;
	}
	public void setCompanyManagerCount(int companyManagerCount) {
		this.companyManagerCount = companyManagerCount;
	}
	public int getSuperManagerCount() {
		return superManagerCount;
	}
	public void setSuperManagerCount(int superManagerCount) {
		this.superManagerCount = superManagerCount;
	}
	public int getOperatorCount() {
		return operatorCount;
	}
	public void setOperatorCount(int operatorCount) {
		this.operatorCount = operatorCount;
	}
	public int getConstructorCount() {
		return constructorCount;
	}
	public void setConstructorCount(int constructorCount) {
		this.constructorCount = constructorCount;
	}
	public int getInspectorCount() {
		return inspectorCount;
	}
	public void setInspectorCount(int inspectorCount) {
		this.inspectorCount = inspectorCount;
	}
	public int getMaintainorCount() {
		return maintainorCount;
	}
	public void setMaintainorCount(int maintainorCount) {
		this.maintainorCount = maintainorCount;
	}
	public int getTestorCount() {
		return testorCount;
	}
	public void setTestorCount(int testorCount) {
		this.testorCount = testorCount;
	}
	public int getUserCount() {
		return userCount;
	}
	public void setUserCount(int userCount) {
		this.userCount = userCount;
	}
	public int getAllCount() {
		return allCount;
	}
	public void setAllCount(int allCount) {
		this.allCount = allCount;
	}
	
	
}
